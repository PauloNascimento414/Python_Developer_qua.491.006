# declaração de variáveis
nome_completo = "Paulo Henrique" # string
numero_inteiro = 18 # int
numero_decimal = 7.5 # float
ativo = True # boolean

# saída de dados
print(type(nome_completo))
print(type(numero_inteiro))
print(type(numero_decimal))
print(type(ativo))

# saída de dados (concatenação - 1º Forma)
print("Valor da variável: ", nome_completo, ". Tipo de dado: ", type(nome_completo))
print("Valor da variável: ", numero_inteiro, ". Tipo de dado: ", type(numero_inteiro))
print("Valor da variável: ", numero_decimal, ". Tipo de dado: ", type(numero_decimal))
print("Valor da variável: ", ativo, ". Tipo de dado: ", type(ativo))

